#--- to delte a space from the begning or the end we use strip()

x = ' Welcome to the world! '
print(x.strip())

#--- to know the length of a string we use len()

y = "Have a wonderful day!"
print(len(y))

#--- lower () returns the given string to lower case if in capital 
z = 'MYDubai'
print(z.lower())

#--- upper() does the oppiste of lower 
a = 'MyDubai'
print(a.upper())

#--- replace() to replace a string with anoother one
b = 'Gradute Lab'
print(b.replace("a","d"))

#--- split() is used to split a string into substrings if it finds a separator 
s = 'The machine was fixed, yesterday morning'
print(s.split(','))
